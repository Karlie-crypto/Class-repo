#include <stdio.h>
/**
 * main - main entry
 * Description: Tell us the output if it's true or false
 * Return: Always 0 success
 */
int main(void)
{
  int a, b;
  if ( a == b && a != b)
    {
      printf("It is true\n ");
    }
  else
    {
      printf("It is false\n ");
    }
  
  return (0);
}

